  function searchKnowledgeBase(query) {
    const q = query.toLowerCase();
    
    // Check for multiple keywords
    const keywords = q.split(' ').filter(word => word.length > 2);
    let bestMatches = [];
    
    for (const entry of KB) {
      const questions = Array.isArray(entry.q) ? entry.q : [entry.q];
      let score = 0;
      
      for (const question of questions) {
        const questionWords = question.toLowerCase().split(' ');
        for (const keyword of keywords) {
          if (questionWords.some(qw => qw.includes(keyword) || keyword.includes(qw))) {
            score++;
          }
        }
      }
      
      if (score > 0) {
        bestMatches.push({ entry, score });
      }
    }
    
    // Sort by score and return best match
    bestMatches.sort((a, b) => b.score - a.score);
    return bestMatches.length > 0 ? bestMatches[0].entry : null;
  }

