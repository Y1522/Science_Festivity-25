  // Function to search booths by age group
  function searchBoothsByAge(ageGroup, isArabic) {
    const ageMap = {
      'ابتدائي': 'age-primary',
      'primary': 'age-primary',
      'إعدادي': 'age-secondary', 
      'preparatory': 'age-secondary',
      'ثانوي': 'age-secondary',
      'secondary': 'age-secondary',
      'روضة': 'age-kindergarten',
      'kindergarten': 'age-kindergarten',
      'ذوى الهمم': 'age-special',
      'special': 'age-special'
    };
    
    const targetAgeClass = ageMap[ageGroup.toLowerCase()];
    if (!targetAgeClass) return null;
    
    const matchingBooths = BOOTH_DATABASE.filter(booth => 
      booth.ar.ageClass === targetAgeClass || 
      booth.en.ageClass === targetAgeClass ||
      booth.ar.age.includes(ageGroup) ||
      booth.en.age.includes(ageGroup)
    );
    
    if (matchingBooths.length === 0) return null;
    
    const lang = isArabic ? 'ar' : 'en';
    let response = isArabic ? 
      `ورش ${ageGroup}! 👦\n\n🎯 الورش المتاحة:\n\n` :
      `Booths for ${ageGroup}! 👦\n\n🎯 Available Booths:\n\n`;
    
    matchingBooths.forEach(booth => {
      response += `• كشك ${booth.id}: ${booth[lang].title} (${booth[lang].duration})\n`;
      response += `🏫 ${booth[lang].description}\n\n`;
    });
    
    response += isArabic ? '🏫 كل الورش دي مناسبة لهذه المرحلة!' : '🏫 All these booths are suitable for this stage!';
    
    return response;
  }

  // Function to get full booth information
  function getBoothInfo(boothId, isArabic) {
    const booth = BOOTH_DATABASE.find(b => b.id === boothId);
    if (!booth) return null;
    
    const lang = isArabic ? 'ar' : 'en';
    return `كشك ${booth.id} - ${booth[lang].title}\n\n` +
           `📝 الوصف: ${booth[lang].description}\n` +
           `⏰ المدة: ${booth[lang].duration}\n` +
           `👥 المرحلة: ${booth[lang].age}\n` +
           `🏛️ الخيمة: خيمة ${booth.tent}\n\n` +
           (isArabic ? '🏫 معلومات مفصلة عن الكشك!' : '🏫 Detailed booth information!');
  }

